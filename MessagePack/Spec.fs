namespace FSMPack.Spec

type Format =
    | PositiveFixInt = 0b00000000uy // - 0x7f
    | FixMap = 0b10000000uy // - 0x8f
    | FixArray = 0b10010000uy // - 0x9f
    | FixStr = 0b10100000uy // - 0xbf
    | Nil = 0b11000000uy
    //| NeverUsed = 0b11000001uy
    | False = 0b11000010uy
    | True = 0b11000011uy
    | Bin8 = 0b11000100uy
    | Bin16 = 0b11000101uy
    | Bin32 = 0b11000110uy
    | Ext8 = 0b11000111uy
    | Ext16 = 0b11001000uy
    | Ext32 = 0b11001001uy
    | Float32 = 0b11001010uy
    | Float64 = 0b11001011uy
    | Uint8 = 0b11001100uy
    | Uint16 = 0b11001101uy
    | Uint32 = 0b11001110uy
    | Uint64 = 0b11001111uy
    | Int8 = 0b11010000uy
    | Int16 = 0b11010001uy
    | Int32 = 0b11010010uy
    | Int64 = 0b11010011uy
    | Fixext = 0b11010100uy
    | Fixext2 = 0b11010101uy
    | Fixext4 = 0b11010110uy
    | Fixext8 = 0b11010111uy
    | Fixext16 = 0b11011000uy
    | Str8 = 0b11011001uy
    | Str16 = 0b11011010uy
    | Str32 = 0b11011011uy
    | Array1 = 0b11011100uy
    | Array32 = 0b11011101uy
    | Map16 = 0b11011110uy
    | Map32 = 0b11011111uy
    | NegativeFixInt = 0b11100000uy
